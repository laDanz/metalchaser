package Classes;

/**
 * A utility class which provides often used colors in <code>Vektor3D</code>
 * format.
 * 
 * @author laDanz
 * 
 */
public class myColor {
	/**
	 * white(1,1,1);
	 */
	static public final Vektor3D WHITE = new Vektor3D(1, 1, 1);
	/**
	 * lack(0,0,0);
	 */
	static public final Vektor3D BLACK = new Vektor3D(0, 0, 0);
	/**
	 * red(1,0,0);
	 */
	static public final Vektor3D RED = new Vektor3D(1, 0, 0);
	/**
	 * green(0,1,0);
	 */
	static public final Vektor3D GREEN = new Vektor3D(0, 1, 0);
	/**
	 * blue(0,0,1);
	 */
	static public final Vektor3D BLUE = new Vektor3D(0, 0, 1);
	/**
	 * yellow (1,1,0)
	 */
	public static final Vektor3D YELLOW = new Vektor3D(1, 1, 0);

	/**
	 * pink (1,0,1)
	 */
	public static final Vektor3D PINK = new Vektor3D(1, 0, 1);

	/**
	 * orange (1,0.5,0)
	 */
	public static final Vektor3D ORANGE = new Vektor3D(1, 0.5, 0);

	/**
	 * grey (0.5,0.5,0.5)
	 */
	public static final Vektor3D GREY = new Vektor3D(0.5, 0.5, 0.5);

	/**
	 * bright grey (0.7,0.7,0.7)
	 */
	public static final Vektor3D BGREY = new Vektor3D(0.7, 0.7, 0.7);

	/**
	 * dark grey (0.3,0.3,0.3)
	 */
	public static final Vektor3D DGREY = new Vektor3D(0.3, 0.3, 0.3);
}
